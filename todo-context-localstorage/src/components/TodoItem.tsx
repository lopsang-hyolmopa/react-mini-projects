import { useState } from "react";

import { useTodo } from "../contexts";
import { TodoType } from "../types";

type Props = {
  todo: TodoType;
};

function TodoItem({ todo }: Props) {
  const [updatedTodoTitle, setUpdatedTodoTitle] = useState<string>(todo.title);
  const [isTodoEditable, setIsTodoEditable] = useState<boolean>(false);

  const { updateTodo, deleteTodo, toogleComplete } = useTodo();

  const toggleCompleted = () => {
    toogleComplete(todo.id);
  };

  const editTodo = () => {
    updateTodo(todo.id, { ...todo, title: updatedTodoTitle });
  };

  return (
    <div
      className={`flex border border-black/10 rounded-lg px-3 py-1.5 gap-x-3 shadow-sm shadow-white/50 duration-300  text-black ${
        todo.isCompleted ? "bg-[#c6e9a7]" : "bg-[#ccbed7]"
      }`}
    >
      <input
        type="checkbox"
        className="cursor-pointer"
        checked={todo.isCompleted}
        onChange={toggleCompleted}
      />
      <input
        type="text"
        className={`border outline-none w-full bg-transparent rounded-lg ${
          isTodoEditable ? "border-black/10 px-2" : "border-transparent"
        } ${todo.isCompleted ? "line-through" : ""}`}
        value={updatedTodoTitle}
        onChange={(e) => setUpdatedTodoTitle(e.target.value)}
        readOnly={!isTodoEditable}
      />
      {/* Edit, Save Button */}
      <button
        className="inline-flex w-8 h-8 rounded-lg text-sm border border-black/10 justify-center items-center bg-gray-50 hover:bg-gray-100 shrink-0 disabled:opacity-50"
        onClick={() => {
          if (todo.isCompleted) return;

          if (isTodoEditable) {
            editTodo();
          } else setIsTodoEditable((prev) => !prev);
        }}
        disabled={todo.isCompleted}
      >
        {isTodoEditable ? "üìÅ" : "‚úèÔ∏è"}
      </button>
      {/* Delete Todo Button */}
      <button
        className="inline-flex w-8 h-8 rounded-lg text-sm border border-black/10 justify-center items-center bg-gray-50 hover:bg-gray-100 shrink-0"
        onClick={() => deleteTodo(todo.id)}
      >
        ‚ùå
      </button>
    </div>
  );
}

export default TodoItem;
